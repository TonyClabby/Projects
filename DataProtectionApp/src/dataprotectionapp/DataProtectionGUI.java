    private void btnRadiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRadiosActionPerformed
        DataProtection myDataProtection = new DataProtection();
        
        File outFile;
        FileWriter fw;
        BufferedWriter bw;
        
        String radioTextQ1 = "";
        String radioTextQ2 = "";
        String radioTextQ3 = "";
        String radioTextQ4 = "";
        int score = 0;
        
        //Selecting output for Question 1
        if(jRadioButton1.isSelected()){
            radioTextQ1 = jRadioButton1.getText();
            answer1 = "Correct";
            score = score+1;
        }
        
        if(jRadioButton2.isSelected()){
            radioTextQ1 = jRadioButton2.getText();
            answer1 = "Wrong";
        }
        
        if(jRadioButton3.isSelected()){
            radioTextQ1 = jRadioButton3.getText();
            answer1 = "Wrong";
        }
        
        //Selecting output for Question 2
        if(jRadioButton4.isSelected()){
            radioTextQ2 = jRadioButton4.getText();
            answer2 = "Wrong";
        }
        
        if(jRadioButton5.isSelected()){
            radioTextQ2 = jRadioButton5.getText();
            answer2 = "Correct";
            score = score+1;
        }
        
        if(jRadioButton6.isSelected()){
            radioTextQ2 = jRadioButton6.getText();
            answer2 = "Wrong";
        }
        
        //Selecting output for Question 3
        if(jRadioButton7.isSelected()){
            radioTextQ3 = jRadioButton7.getText();
            answer3 = "Wrong";
        }
        
        if(jRadioButton8.isSelected()){
            radioTextQ3 = jRadioButton8.getText();
            answer3 = "Wrong";
        }
        
        if(jRadioButton9.isSelected()){
            radioTextQ3 = jRadioButton9.getText();
            answer3 = "Correct";
            score = score+1;
        }
        
        //Selecting output for Question 4
        if(jRadioButton10.isSelected()){
            radioTextQ4 = jRadioButton10.getText();
            answer4 = "Wrong";
        }
        
        if(jRadioButton11.isSelected()){
            radioTextQ4 = jRadioButton11.getText();
            answer4 = "Correct";
            score = score+1;
        }
        
        if(jRadioButton12.isSelected()){
            radioTextQ4 = jRadioButton12.getText();
            answer4 = "Wrong";
        }
        
        //Set Variables
        myDataProtection.setAnswer1(answer1);
        myDataProtection.setAnswer2(answer2);
        myDataProtection.setAnswer3(answer3);
        myDataProtection.setAnswer4(answer4);
        myDataProtection.setScore(score);
        
        //Push into the array
        myArray[count]=myDataProtection;
        count++;
        
        //Creating an instance of the file and setting up a new file
        try{
        outFile = new File("Answers.txt");
        fw = new FileWriter(outFile, true);
        bw = new BufferedWriter(fw);
        
        //Write to the file
        bw.write(radioTextQ1);
        bw.newLine();
        bw.write(radioTextQ2);
        bw.newLine();
        bw.write(radioTextQ3);
        bw.newLine();
        bw.write(radioTextQ4);
        bw.newLine();
        bw.newLine();
        
        //Close the stream
        bw.close();
        }
        //Error message in case of exception
        catch(IOException e){
            JOptionPane.showMessageDialog(null, "Error writing the file.");
        }
    }//GEN-LAST:event_btnRadiosActionPerformed

