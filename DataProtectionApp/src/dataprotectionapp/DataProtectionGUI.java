    private void navBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_navBtnActionPerformed
        
        //Loop to navigate through the array
        for(int i=0;i<count;i++){
                DataProtection myDataProtection = new DataProtection ();

                myDataProtection=myArray[i];
                
                //Output from the array
                JOptionPane.showMessageDialog(null, "Q1: "+myDataProtection.getAnswer1());
                JOptionPane.showMessageDialog(null, "Q2: "+myDataProtection.getAnswer2());
                JOptionPane.showMessageDialog(null, "Q3: "+myDataProtection.getAnswer3()); 
                JOptionPane.showMessageDialog(null, "Q4: "+myDataProtection.getAnswer4());
                JOptionPane.showMessageDialog(null, "Overall Score: "+myDataProtection.getScore());
                navBtn.setVisible(false);
            }
    }//GEN-LAST:event_navBtnActionPerformed

